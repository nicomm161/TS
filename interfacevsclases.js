"use strict";
// Interfaces vs Clases en TypeScript
// Interfaces en TypeScript
// Las interfaces se utilizan para definir la estructura de un objeto.
// No proporcionan implementación, solo definen qué propiedades y métodos debe tener un objeto.
// Las interfaces son útiles para definir tipos y estructuras de datos, 
// asegurando que los objetos sigan una estructura específica.
// Clases en TypeScript
// Las clases son plantillas para crear objetos con propiedades y métodos específicos.
// Proporcionan tanto la estructura como la implementación de las propiedades y métodos.
// Las clases permiten la creación de múltiples objetos con la misma estructura y comportamiento.
// Pueden extender otras clases (herencia) y heredar su comportamiento.
// Diferencias Clave entre Interfaces y Clases
// 1. Propósito:
//    - Interfaces: Definen la estructura de un objeto, especificando qué propiedades y métodos debe tener.
//    - Clases: Son plantillas para crear objetos, proporcionando tanto la estructura como la implementación.
// 2. Implementación:
//    - Interfaces: No proporcionan implementación, solo definen la forma del objeto.
//    - Clases: Incluyen implementación concreta de propiedades y métodos.
// 3. Herencia:
//    - Interfaces: Pueden extender otras interfaces, permitiendo la reutilización de definiciones.
//    - Clases: Pueden extender otras clases y heredar su comportamiento.
// 4. Uso:
//    - Interfaces: Útiles para definir tipos y estructuras de datos sin preocuparse por la lógica de implementación.
//    - Clases: Útiles para crear instancias de objetos con comportamiento y estado específicos, y son esenciales en la programación orientada a objetos.
